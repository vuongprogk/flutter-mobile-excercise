void main() {
  // Integer
  int myInt = 42;
  print('Integer: $myInt');

  // Double
  double myDouble = 3.14;
  print('Double: $myDouble');

  // String
  String myString = 'Hello, Dart!';
  print('String: $myString');

  // List
  List<int> myList = [1, 2, 3, 4, 5];
  print('List: $myList');

  // Map
  Map<String, int> myMap = {'one': 1, 'two': 2, 'three': 3};
  print('Map: $myMap');

  // Set
  Set<String> mySet = {'apple', 'banana', 'cherry'};
  print('Set: $mySet');
}
